package DB;

public class BuffaloRate {

    private long id;
    private int d;
    private double fat, rate;

    public int getD() {
        return d;
    }

    public void setD(int d) {
        this.d = d;
    }

    public double getFat() {
        return fat;
    }

    public void setFat(double fat) {
        this.fat = fat;
    }

    public double getRate() {
        return rate;
    }

    public void setRate(double rate) {
        this.rate = rate;
    }

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }
}
